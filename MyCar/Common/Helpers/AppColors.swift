import UIKit

enum AppColors {
    /// Transparent
    static let clear        = UIColor.clear
    /// Color hex - #FFFFFF
    static let white        = UIColor(named: "white") ?? UIColor(hex: 0xFFFFFF)
    /// Color hex - #000000
    static let black        = UIColor(named: "black") ?? UIColor(hex: 0x000000)
}
